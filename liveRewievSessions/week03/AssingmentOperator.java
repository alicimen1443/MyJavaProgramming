package week03;

public class AssingmentOperator {

    public static void main(String[] args) {

        int i = 100;
        System.out.println("i = " + i);

        i+=10 ; // i= i+10;

        System.out.println("i = " + i);

        i-=10;
        System.out.println("i = " + i);

        i*=3;
        System.out.println("i = " + i);

        i/=4;

        System.out.println("i = " + i);

        i%=5; // re-assign i value change each time
        System.out.println("i = " + i);

        String statement= "EU8";

        statement+= "Great";

        System.out.println("statement = " + statement);



    }
}
