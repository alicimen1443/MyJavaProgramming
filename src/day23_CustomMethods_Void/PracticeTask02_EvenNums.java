package day23_CustomMethods_Void;

public class PracticeTask02_EvenNums {
    public static void main(String[] args) {

        evenNums();

    }

    public static void evenNums(){
        for (int i = 2; i <101 ; i+=2) {
            System.out.print(i+" ");
        }
    }
}

