package day17_While_DoWhile;

public class WhileLoopIntro {
    public static void main(String[] args) {

        if(true){
            System.out.println("Wooden Spoon"); // execution: 1 time
        }


        System.out.println("----------------------------------");

        while(true){  // while loop is a repeated if statement
            System.out.println("Wooden Spoon"); //
        }


    }
}
